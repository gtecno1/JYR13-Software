public class auditoriar2 {
  
    private String ce;
    private String pro; 
    private String act;
    private String hor;
    private String fecha;

    public auditoriar2(String ce, String pro, String act, String hor, String fecha) {
        this.ce = ce;
        this.pro = pro;
        this.act = act;
        this.hor = hor;
        this.fecha = fecha;
    }

    public String getAct() {
        return act;
    }

    public String getCe() {
        return ce;
    }

    public String getFecha() {
        return fecha;
    }

    public String getHor() {
        return hor;
    }

    public String getPro() {
        return pro;
    }
    

   
   

   

  
}
